package com.javaex.practice;

public class Ex14 {

	public static void main(String[] args) {
		int a = 5;
		
		System.out.println(a);
		System.out.println(++a);
		System.out.println(a);
		System.out.println(a++);
		System.out.println(a);
		System.out.println(--a);
		System.out.println(a);
		System.out.println(a--);
		System.out.println(a);
	}
/*
 * 예상된 최종값: 5
 * 예상과 같았음. 
 */
}
